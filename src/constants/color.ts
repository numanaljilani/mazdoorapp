export const border_color = (mode: boolean) =>{
 return  mode ? "border-gray-200" : "border-gray-200"
}
export const bg_color = (mode: boolean) =>{
 return   mode ? "bg-[#1F1F1F]" : "bg-[#FFFFFF]"
}
export const bg_color2 = (mode: boolean) =>{
 return   mode ? "bg-[#2E2E2E]" : "bg-gray-50"
}
export const text_color = (mode: boolean) =>{
 return mode  ? "text-[#FFFF]" : "text-black"
}
export const secondary_text_color = (mode: boolean) =>{
 return mode  ? "text-[#A4A4A4]" : "text-[#3b3941]"
}